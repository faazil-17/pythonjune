num1=5

num2=10

addition_res=num1 + num2

print(f"{num1} + {num2}={addition_res}")


substraction_res=num1-num2

print(f"{num1} - {num2}={substraction_res}")

mutiplication_res=num1*num2

print(f"{num1} * {num2}={mutiplication_res}")

division_res=num1/num2

print(f"{num1}/{num2}={division_res}")


modules_res=num1%num2

print(f"{num1} % {num2}={modules_res}")

exponent_res=num1**2

print(f"{num1} ^ {2}={exponent_res}")



